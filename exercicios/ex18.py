# 18. Faça um programa que peça ao usuário três números e verifique se todos são positivos

numero1 = int(input('diga um numero '))
numero2 = int(input('diga outro numero '))
numero3 = int(input('diga outro numero '))

if numero1>0 and numero2>0 and numero3 > 0:
    print('todos seus numeros sao positivos')

else:
    print('seus numeros nao sao positivos')












