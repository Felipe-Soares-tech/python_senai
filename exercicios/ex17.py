# 17. Crie um programa que solicite ao usuário dois números e exiba a soma, subtração, multiplicação e divisão entre eles.


numero1 = int(input('diga um numero '))
numero2 = int(input('diga outro numero '))

soma = numero1 + numero2
subtracao = numero1 - numero2
multi = numero1 * numero2 
divsao = numero1 / numero2

print(f'divisao {divsao}, multiplicação {multi}, adição {soma}, subtração {subtracao}')
















